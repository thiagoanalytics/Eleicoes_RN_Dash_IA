#No Prefect, os principais blocos são:
    #Flow → Representa sua pipeline inteira.
    #Task → São os passos individuais dentro da pipeline (ex: extrair dados, transformar, carregar).
    #Deployment → Configuração para executar o flow em diferentes ambientes (local, nuvem, agendado, etc.).
    #Agent → Quem executa os flows (pode rodar localmente, no Docker, Kubernetes, etc.).


